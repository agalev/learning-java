public class Conditionals {
  public static void main(String[] args) {
    int a = 4;
    int b = 5;
    boolean result;
    if (a == 4) {
      System.out.println("a equals four");
    }
    result = a < b;
    System.out.println(result);
    result = a > b;
    System.out.println(result);
    System.out.println(result);
  }
}
